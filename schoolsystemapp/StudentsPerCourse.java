
package schoolsystemapp;

import java.util.ArrayList;

/**
 *
 * @author elTok
 */
public class StudentsPerCourse {
 private  Course c;
 private ArrayList<Student> students;

    public StudentsPerCourse(Course c, ArrayList<Student> students) {
        this.c = c;
        this.students = students;
    }

    @Override
    public String toString() {
        return "StudentsPerCourse{" + "c=" + c + ", students=" + students + '}';
    }

   
   

    

   

   
   

   
   

   
   

   
       
}

